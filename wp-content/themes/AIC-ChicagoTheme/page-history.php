                special celebrations, wakes, commemorative dinners and countless special events. When Chicago’s Indians need
                to get together, this is where they come. In a similar vein, AIC has been the point of origin for a number
                of experimental and successful educational programs. We have seen elementary school, high school, headstart,
                tutorial, and summer day camp programs take root and flourish here; many of them have gone on to successful
                independent operation out of our building. We have been the site of gift shops, art galleries, and theaters.</pr>


